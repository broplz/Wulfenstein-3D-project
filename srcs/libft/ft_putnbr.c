#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == MIN_INT)
		ft_putstr(C_MIN_INT);
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
}
